/**
 * This file demonstrates a basic object-oriented structure in Scala using braces for the object body.
 * The object below contains a main method, which serves as the entry point of the program.
 */
package oostructurewithbraces

/**
 * Defines a singleton object with a main method using braces.
 * The main method prints a greeting message.
 */
object HelloWorldBraces {
    def main(args: Array[String]) = {
        println("Hello, World with Braces!")
    }
}